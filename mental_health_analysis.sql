CREATE TABLE PEOPLE(
     PERSON_ID INT PRIMARY KEY,
     NAME VARCHAR(50),
     AGE INT CHECK(AGE BETWEEN 18 AND 60),
     GENDER VARCHAR(10) 
     CHECK(GENDER IN('MALE','FEMALE','OTHERS')),
     CITY VARCHAR(50)
);

CREATE TABLE MENTAL_HEALTH_RECORDS (
    RECORD_ID INT PRIMARY KEY,
    PERSON_ID INT,
    SLEEP_HOURS INT CHECK(SLEEP_HOURS BETWEEN 1 AND 12),
    STRESS_LEVEL INT CHECK(STRESS_LEVEL BETWEEN 1 AND 10),
    WORK_PRESSURE INT CHECK(WORK_PRESSURE BETWEEN 1 AND 10),
    ANXIETY_LEVEL INT CHECK(ANXIETY_LEVEL BETWEEN 1 AND 10),
    DEPRESSION_LEVEL INT CHECK(DEPRESSION_LEVEL BETWEEN 1 AND 10),
    EXERCISE_HOURS INT CHECK(EXERCISE_HOURS BETWEEN 0 AND 5),
    SCREEN_TIME INT CHECK(SCREEN_TIME BETWEEN 1 AND 15),

    FOREIGN KEY (PERSON_ID)
    REFERENCES PEOPLE(PERSON_ID)
);

SHOW TABLES;
DESC MENTAL_HEALTH_RECORDS;


/*CITY HEALTH OVERVIEW (Avg Stress by City)*/
SELECT P.CITY,
       AVG(M.STRESS_LEVEL) AS AVG_STRESS
FROM PEOPLE AS P
JOIN MENTAL_HEALTH_RECORDS AS M
ON P.PERSON_ID=M.PERSON_ID
GROUP BY P.CITY
ORDER BY AVG_STRESS DESC;

/*DOES LESS SLEEP INCREASE STRESS LEVELS*/
SELECT 
      CASE 
          WHEN M.SLEEP_HOURS < 5 THEN 'LOW SLEEP'
          WHEN M.SLEEP_HOURS BETWEEN 5 AND 7 THEN 'MODERATE SLEEP'
          ELSE 'GOOD SLEEP'
          END AS SLEEP_CATEGORY,
          AVG(M.STRESS_LEVEL) AS AVG_STRESS
	FROM MENTAL_HEALTH_RECORDS AS M
    GROUP BY SLEEP_CATEGORY;
    
    /*“How does work pressure affect anxiety levels?”*/
    SELECT
         CASE
            WHEN M.WORK_PRESSURE>7 THEN 'HIGH PRESSURE'
            ELSE 'NORMAL PRESSURE'
		END AS PRESSURE_CATEGORY,
        AVG(M.ANXIETY_LEVEL) AS AVG_ANXIETY
FROM MENTAL_HEALTH_RECORDS AS M
GROUP BY PRESSURE_CATEGORY;

/*“Does high screen time affect mental health?”*/
SELECT 
     CASE
         WHEN M.SCREEN_TIME >10 THEN 'HIGH SCREEN'
         ELSE 'NORMAL SCREEN TIME'
	END AS SCREEN_GROUP,
    AVG(M.DEPRESSION_LEVEL) AS AVG_DEPRESSION
FROM MENTAL_HEALTH_RECORDS AS M
GROUP BY SCREEN_GROUP;

/*“Does exercise reduce depression?”*/
SELECT M.EXERCISE_HOURS,
       AVG(M.DEPRESSION_LEVEL) AS AVG_DEPRESSSION
FROM MENTAL_HEALTH_RECORDS AS M
GROUP BY M.EXERCISE_HOURS
ORDER BY M.EXERCISE_HOURS;

/*“Identify individuals at risk of mental health issues”*/
SELECT P.NAME,
       P.CITY,
       M.STRESS_LEVEL,
       M.SLEEP_HOURS,
       M.ANXIETY_LEVEL,
       M.SCREEN_TIME
FROM PEOPLE AS P
JOIN MENTAL_HEALTH_RECORDS AS M
ON P.PERSON_ID=M.PERSON_ID
WHERE  M.STRESS_LEVEL>7
     AND  M.SLEEP_HOURS< 5
     AND   M.ANXIETY_LEVEL>7
     AND   M.SCREEN_TIME >10;
     
/*“Which gender has higher average stress and anxiety?”*/
SELECT P.GENDER,
       AVG(M.STRESS_LEVEL) AS AVG_STRESS,
       AVG(M.ANXIETY_LEVEL) AS AVG_ANXIETY
FROM PEOPLE AS P
JOIN MENTAL_HEALTH_RECORDS AS M
ON P.PERSON_ID=M.PERSON_ID
GROUP BY P.GENDER;

/*“Which city has highest work pressure + stress combined?”*/
SELECT P.CITY,
       AVG(M.WORK_PRESSURE) AS AVG_WORK_PRESSURE,
       AVG(M.STRESS_LEVEL) AS AVG_STRESS
FROM PEOPLE P
JOIN MENTAL_HEALTH_RECORDS M
ON P.PERSON_ID = M.PERSON_ID
GROUP BY P.CITY
ORDER BY AVG_STRESS DESC;

/*HEALTHY VS UNHEALTHY PEOPLE COUNT*/
SELECT 
    CASE 
        WHEN M.STRESS_LEVEL <= 5 
         AND M.SLEEP_HOURS >= 6 
         AND M.ANXIETY_LEVEL <= 5 
        THEN 'HEALTHY'
        ELSE 'UNHEALTHY'
    END AS HEALTH_STATUS,
    COUNT(*) AS TOTAL_PEOPLE
FROM MENTAL_HEALTH_RECORDS M
GROUP BY HEALTH_STATUS;

/*MENTAL HEALTH RISK SCORE*/
SELECT P.NAME,
       P.CITY,
       (M.STRESS_LEVEL * 2 +
        M.ANXIETY_LEVEL * 2 +
        M.DEPRESSION_LEVEL * 2 +
        M.WORK_PRESSURE * 1.5 -
        M.EXERCISE_HOURS * 2 -
        M.SLEEP_HOURS) AS RISK_SCORE
FROM PEOPLE P
JOIN MENTAL_HEALTH_RECORDS M
ON P.PERSON_ID = M.PERSON_ID
ORDER BY RISK_SCORE DESC;